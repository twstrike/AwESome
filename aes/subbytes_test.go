package aes

import (
	. "gopkg.in/check.v1"
)

type SubBytesTestSuite struct{}

var _ = Suite(&SubBytesTestSuite{})

func (s *SubBytesTestSuite) TestMe(c *C) {
	oldstate := state{
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00,
	}
	newState := state{
		0x63, 0x63, 0x63, 0x63,
		0x63, 0x63, 0x63, 0x63,
		0x63, 0x63, 0x63, 0x63,
		0x63, 0x63, 0x63, 0x63,
	}
	c.Assert(subBytes(oldstate), Equals, newState)
}

